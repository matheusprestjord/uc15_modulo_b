    public void preencherTabela() {
        FluxoCaixaDAO fluxoCaixaDAO = new FluxoCaixaDAO();

        List<FluxoCaixa> fluxos = fluxoCaixaDAO.listarTodos();

        DefaultTableModel tabelaFluxos = (DefaultTableModel) tblControleCaixa.getModel();

        tabelaFluxos.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

        tblControleCaixa.setRowSorter(new TableRowSorter(tabelaFluxos));

        for (FluxoCaixa f : fluxos) {
            Object[] obj = new Object[]{
                f.getId(),
                f.getData(),
                f.getTipo(),
                f.getValor(),
                f.getDescricao()
            };
            tabelaFluxos.addRow(obj);
        }
    }


