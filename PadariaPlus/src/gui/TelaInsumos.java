    public void preencherTabela() {
        InsumoDAO insumoDAO = new InsumoDAO();

        List<Insumo> insumos = insumoDAO.listarTodos();

        DefaultTableModel tabelaInsumos = (DefaultTableModel) tblInsumos.getModel();

        tabelaInsumos.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

        tblInsumos.setRowSorter(new TableRowSorter(tabelaInsumos));

        for (Insumo i : insumos) {
            Object[] obj = new Object[]{
                i.getId(),
                i.getNome(),
                i.getQuantidade(),
                i.getPreco(),
                i.getDataValidade(),
                i.getFornecedor(),
                i.getDataEntrada()
            };
            tabelaInsumos.addRow(obj);
        }
    }
    
