    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // Verificar se os campos estão vazios
        if (!String.valueOf(cmbBoxTipo.getSelectedItem()).equals("Selecione um tipo...")
                && !fmtTxtData.getText().trim().isEmpty()
                && !txtADescricao.getText().trim().isEmpty()) {
            // Criar o objeto insumo
            Relatorio relatorio = new Relatorio();

            // Atribuir os valores dos campos à variáveis
            String tipoRelatorio = String.valueOf(cmbBoxTipo.getSelectedItem());
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataGeracao = LocalDate.parse(fmtTxtData.getText(), formato);
            String descricao = txtADescricao.getText();

            // Atribuir os valores ao objeto
            relatorio.setTipoRelatorio(tipoRelatorio);
            relatorio.setDataGeracao(dataGeracao);
            relatorio.setDescricao(descricao);

            RelatorioDAO relatorioDAO = new RelatorioDAO();
            relatorioDAO.enviarRelatorio(relatorio);

            // Limpar os campos
            cmbBoxTipo.setSelectedIndex(0);
            fmtTxtData.setText("");
            txtADescricao.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Relatório enviado com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (!txtRemover.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtRemover.getText());

            RelatorioDAO relatorioDAO = new RelatorioDAO();
            relatorioDAO.removerRelatorio(id);

            // Limpar o campo
            txtRemover.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Relatório removido com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

