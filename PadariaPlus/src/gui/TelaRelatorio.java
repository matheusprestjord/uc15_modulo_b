    public void preencherTabela() {
        RelatorioDAO relatorioDAO = new RelatorioDAO();

        List<Relatorio> relatorios = relatorioDAO.listarTodos();

        DefaultTableModel tabelaRelatorios = (DefaultTableModel) tblRelatorios.getModel();

        tabelaRelatorios.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

        tblRelatorios.setRowSorter(new TableRowSorter(tabelaRelatorios));

        for (Relatorio r : relatorios) {
            Object[] obj = new Object[]{
                r.getId(),
                r.getTipoRelatorio(),
                r.getDataGeracao(),
                r.getDescricao(),
            };
            tabelaRelatorios.addRow(obj);
        }
    }

