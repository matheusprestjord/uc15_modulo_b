    // Método para preencher a tabela
    public void preencherTabela() {
        ReceitaDAO receitaDAO = new ReceitaDAO();

        List<Receita> receitas = receitaDAO.listarTodos();

        DefaultTableModel tabelaReceitas = (DefaultTableModel) tblReceitas.getModel();

        tabelaReceitas.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

        tblReceitas.setRowSorter(new TableRowSorter(tabelaReceitas));

        for (Receita r : receitas) {
            Object[] obj = new Object[]{
                r.getId(),
                r.getNome(),
                r.getModoPreparo()
            };
            tabelaReceitas.addRow(obj);
        }
    }

