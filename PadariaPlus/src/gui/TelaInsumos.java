    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (!txtNome.getText().trim().isEmpty() &&
                !fmtTxtQuantidade.getText().trim().isEmpty() &&
                !fmtTxtPreco.getText().trim().isEmpty() &&
                !fmtTxtDataValidade.getText().trim().isEmpty() &&
                !txtFornecedor.getText().trim().isEmpty() &&
                !fmtTxtDataEntrada.getText().trim().isEmpty()) {
            // Criar o objeto insumo
            Insumo insumo = new Insumo();

            // Atribuir os valores dos campos à variáveis
            String nome = txtNome.getText();
            double quantidade = Double.parseDouble(fmtTxtQuantidade.getText().replace(",", "."));
            double preco = Double.parseDouble(fmtTxtPreco.getText().replace(",", "."));
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataValidade = LocalDate.parse(fmtTxtDataValidade.getText(), formato);
            String fornecedor = txtFornecedor.getText();
            LocalDate dataEntrada = LocalDate.parse(fmtTxtDataEntrada.getText(), formato);

            // Atribuir os valores ao objeto
            insumo.setNome(nome);
            insumo.setQuantidade(quantidade);
            insumo.setPreco(preco);
            insumo.setDataValidade(dataValidade);
            insumo.setFornecedor(fornecedor);
            insumo.setDataEntrada(dataEntrada);

            InsumoDAO insumoDAO = new InsumoDAO();
            insumoDAO.cadastrarInsumo(insumo);

            // Limpar os campos
            txtNome.setText("");
            fmtTxtQuantidade.setText("");
            fmtTxtPreco.setText("");
            fmtTxtDataValidade.setText("");
            txtFornecedor.setText("");
            fmtTxtDataEntrada.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Insumo cadastrado com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnUsarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsarActionPerformed
        if (!txtUsarInsumo.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtUsarInsumo.getText());
            double quantidade = (Double) spnUsarInsumo.getValue();

            InsumoDAO insumoDAO = new InsumoDAO();
            insumoDAO.diminuirInsumo(id, quantidade);

            // Limpar o campo
            txtUsarInsumo.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Insumo vendido com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUsarActionPerformed

    private void btnRemoverInsumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverInsumoActionPerformed
        // Verificar se o campo está vazio
        if (!txtRemoverInsumo.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtRemoverInsumo.getText());

            InsumoDAO insumoDAO = new InsumoDAO();
            insumoDAO.removerInsumo(id);

            // Limpar o campo
            txtRemoverInsumo.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Insumo removido com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverInsumoActionPerformed

