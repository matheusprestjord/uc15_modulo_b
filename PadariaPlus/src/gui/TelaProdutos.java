    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Verificar se os campos estão vazios
        if (!txtNome.getText().trim().isEmpty()
                && !fmtTxtQuantidade.getText().trim().isEmpty()
                && !fmtTxtPreco.getText().trim().isEmpty()
                && !fmtTxtDataValidade.getText().trim().isEmpty()) {

            // Só depois de verificar, faça as conversões
            Produto produto = new Produto();
            String nome = txtNome.getText();
            double quantidade = Double.parseDouble(fmtTxtQuantidade.getText().replace(",", "."));
            double preco = Double.parseDouble(fmtTxtPreco.getText().replace(",", "."));
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataValidade = LocalDate.parse(fmtTxtDataValidade.getText(), formato);

            // Atribuir os valores ao objeto
            produto.setNome(nome);
            produto.setQuantidade(quantidade);
            produto.setPreco(preco);
            produto.setDataValidade(dataValidade);

            ProdutoDAO produtoDAO = new ProdutoDAO();
            produtoDAO.cadastrarProduto(produto);

            // Limpar os campos
            txtNome.setText("");
            fmtTxtQuantidade.setText("");
            fmtTxtPreco.setText("");
            fmtTxtDataValidade.setText("");

            // Atualizar a tabela
            preencherTabela();

            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        // Verificar se o campo está em branco
        if (!txtVenderProduto.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtVenderProduto.getText());
            int quantidade = (Integer) spnVenderProduto.getValue();

            ProdutoDAO produtoDAO = new ProdutoDAO();
            produtoDAO.venderProduto(id, quantidade);

            // Limpar o campo
            txtVenderProduto.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Produto vendido com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnVenderActionPerformed

    private void btnRemoverProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverProdutoActionPerformed
        // Verificar se o campo está em branco
        if (!txtRemoverProduto.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtRemoverProduto.getText());

            ProdutoDAO produtoDAO = new ProdutoDAO();
            produtoDAO.removerProduto(id);

            // Limpar o campo
            txtRemoverProduto.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Produto removido com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverProdutoActionPerformed

