    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (!txtRemover.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtRemover.getText());

            ReceitaDAO receitaDAO = new ReceitaDAO();
            receitaDAO.removerReceita(id);

            // Limpar o campo
            txtRemover.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Receita removida com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Verificar se os campos estão vazios
        if (!txtNome.getText().trim().isEmpty() &&
            !txtAModoPreparo.getText().trim().isEmpty()) {
            // Criar o objeto insumo
            Receita receita = new Receita();

            // Atribuir os valores dos campos à variáveis
            String nome = txtNome.getText();
            String modoPreparo = txtAModoPreparo.getText();

            // Atribuir os valores ao objeto
            receita.setNome(nome);
            receita.setModoPreparo(modoPreparo);

            ReceitaDAO receitaDAO = new ReceitaDAO();
            receitaDAO.adicionarReceita(receita);

            // Limpar os campos
            txtNome.setText("");
            txtAModoPreparo.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Relatório enviado com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

