    public void preencherTabela() {
        ProdutoDAO produtosDAO = new ProdutoDAO();

        List<Produto> produtos = produtosDAO.listarTodos();

        DefaultTableModel tabelaProdutos = (DefaultTableModel) tblProdutos.getModel();

        tabelaProdutos.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

        tblProdutos.setRowSorter(new TableRowSorter(tabelaProdutos));

        for (Produto p : produtos) {
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getQuantidade(),
                p.getPreco(),
                p.getDataValidade()
            };
            tabelaProdutos.addRow(obj);
        }
    }

