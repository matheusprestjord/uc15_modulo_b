    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (!txtRemover.getText().trim().isEmpty()) {
            // Pegar o ID do campo
            int id = Integer.parseInt(txtRemover.getText());

            FluxoCaixaDAO fluxoCaixaDAO = new FluxoCaixaDAO();
            fluxoCaixaDAO.remover(id);

            // Limpar o campo
            txtRemover.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Receita removida com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Verificar se os campos estão vazios
        if (!fmtTxtData.getText().trim().isEmpty() &&
            !String.valueOf(cmbBoxTipo.getSelectedItem()).equals("Selecione um produto...") &&
            !fmtTxtValor.getText().trim().isEmpty() &&
            !txtADescricao.getText().trim().isEmpty()) {
                
            // Criar o objeto insumo 
            FluxoCaixa fluxoCaixa = new FluxoCaixa();

            // Atribuir os valores dos campos à variáveis
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data = LocalDate.parse(fmtTxtData.getText(), formato);
            String tipo = String.valueOf(cmbBoxTipo.getSelectedItem());
            double valor = Double.parseDouble(fmtTxtValor.getText().replace(",", "."));
            String descricao = txtADescricao.getText();

            // Atribuir os valores ao objeto
            fluxoCaixa.setData(data);
            fluxoCaixa.setTipo(tipo);
            fluxoCaixa.setValor(valor);
            fluxoCaixa.setDescricao(descricao);

            FluxoCaixaDAO fluxoCaixaDAO = new FluxoCaixaDAO();
            fluxoCaixaDAO.adicionarFluxoCaixa(fluxoCaixa);

            // Limpar os campos
            fmtTxtData.setText("");
            cmbBoxTipo.setSelectedIndex(0);
            fmtTxtValor.setText("");
            txtADescricao.setText("");

            // Atualizar a tabela
            preencherTabela();

            // Mostrar mensagem de êxito
            JOptionPane.showMessageDialog(this, "Fluxo enviado com sucesso!");
        } else {
            // Exibir mensagem de aviso
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

